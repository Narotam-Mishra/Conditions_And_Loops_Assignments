
// Example for if statement

let num1 = 10;

if (num1 > 0) {
    console.log("The number is positive.");
}


// Example for if-else statement

let num2 = -5;

if (num2 > 0) {
    console.log("The number is positive.");
} else {
    console.log("The number is negative.");
}

// Example for if-else if-else statement

let num3 = 0;

if (num3 > 0) {
    console.log("The number is positive.");
} else if (num3 < 0) {
    console.log("The number is negative.");
} else {
    console.log("The number is zero.");
}

